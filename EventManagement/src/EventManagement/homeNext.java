    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        Connection con = myConnection(); 
        id=Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(),0).toString());
        String fd=jTable1.getValueAt(jTable1.getSelectedRow(),3).toString();
        String st=jTable1.getValueAt(jTable1.getSelectedRow(),4).toString();
        String ad=jTable1.getValueAt(jTable1.getSelectedRow(),7).toString();
        int am=id;
        geteventid.setText(String.valueOf(am));
        try{
            PreparedStatement prest;
            String sql ="select * from event_details where eid="+id;
            prest = con.prepareStatement(sql);
            ResultSet rs = prest.executeQuery();
            while(rs.next()){
                getname.setText(rs.getString(2));
                date1.setDate(rs.getDate(3));
                phone.setText(rs.getString(8));                
                address.setText(rs.getString(9));
                decoration.setText(rs.getString(11));
                switch(fd){
                    case "VEG":
                        food_details1.setSelectedIndex(0);
                        break;
                    case "NON -VEG":
                        food_details1.setSelectedIndex(1);
                        break;
                    case "BOTH":
                        food_details1.setSelectedIndex(2);
                        break;
                    default:
                        food_details1.setSelectedIndex(2);
                }
                switch(st){
                    case "OPEN AIR":
                        stage_details.setSelectedIndex(0);
                        break;
                    case "BALLOONS":
                        stage_details.setSelectedIndex(1);
                        break;
                    case "FLOWERS":
                        stage_details.setSelectedIndex(2);
                        break;
                    default:
                        stage_details.setSelectedIndex(2);
                }
                switch(ad){
                    case "100":
                        audience1.setSelectedIndex(0);
                        break;
                    case "200":
                        audience1.setSelectedIndex(1);
                        break;
                    case "300":
                        audience1.setSelectedIndex(2);
                        break;
                    default:
                        audience1.setSelectedIndex(2);  
                }
            }
            rs.close();
            prest.close();
        }catch (SQLException ex) {
            Logger.getLogger(homeNext.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTable1MouseClicked

