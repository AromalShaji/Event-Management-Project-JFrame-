    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        Connection con = myConnection();
        if(id!=0){
        try{
            String getname1=getname.getText();
                
            if(((JTextField)date1.getDateEditor().getUiComponent()).getText().isEmpty()){
                show_date.setText("Invalid");
            }
            else{
                SimpleDateFormat sdf1 = new SimpleDateFormat("yyyy-MM-dd");
                date=sdf1.format(date1.getDate());
                date3=sdf1.parse(date);
                date_vali=sdf1.parse(date);
                
                SimpleDateFormat sss = new SimpleDateFormat("yyyy-MM-dd");
                date2=new Date();
                cdate=sss.format(date2);
                date_vali2=sss.parse(cdate);
            
                
                if(date2.compareTo(date_vali)>=date3.compareTo(date_vali2)){
                    show_date.setText("Invalid");
                }
                else{
                    show_date.setText("");
                }
            }
            String food_value = food_details1.getSelectedItem().toString();
            String stage_value = stage_details.getSelectedItem().toString();
            String phone1=phone.getText();
            String address1=address.getText();
            String audience = audience1.getSelectedItem().toString();
            String decoration1=decoration.getText();
                
            PreparedStatement prest,prest1,prest2,prest3;
            
            
            String a1=audience;
            String sq3="SELECT * FROM audience_details where audience_name='"+a1+"'";
            prest2= con.prepareStatement(sq3);
            ResultSet rs2 = prest2.executeQuery(sq3);
            while(rs2.next()){
                a_rate = String.valueOf(rs2.getInt("audience_rate"));
                ar=Integer.parseInt(a_rate);
            }
            //food
            String f1=food_value;
            String sq2="SELECT * FROM food_details where food_name='"+f1+"'";
            prest1= con.prepareStatement(sq2);
            ResultSet rs1 = prest1.executeQuery(sq2);
            while(rs1.next()){
                f_rate = String.valueOf(rs1.getInt("food_rate"));
                fr=Integer.parseInt(f_rate);
            }
            //stage
            String s1=stage_value;
            String sq4="SELECT * FROM stage_details where stage_name='"+s1+"'";
            prest3= con.prepareStatement(sq4);
            ResultSet rs3 = prest3.executeQuery(sq4);
            while(rs3.next()){
                s_rate = String.valueOf(rs3.getInt("stage_rate"));
                sr=Integer.parseInt(s_rate);
            }
            total=ar+fr+sr;
            
            String PATTERN ="^\\d{10}$";
            Pattern p=Pattern.compile(PATTERN);
            Matcher m =p.matcher(phone.getText());
            
            if(m.matches()){
                show_phone.setText("");
            }
            else{
                show_phone.setText("Invalid");
            }
            
            if(getname1.equals("")||address1.equals("")||phone1.equals("")||phone1.length()!=10||!m.matches()||((JTextField)date1.getDateEditor().getUiComponent()).getText().isEmpty()||date2.compareTo(date_vali)>=date3.compareTo(date_vali2)){
                okcancel("All field need to be filled!");
            }
            else{
            String sql ="update event_details set user_name='"+getname1+"',event_date='"+date+"',food='"+food_value+"',stage='"+stage_value+"',phone='"+phone1+"',address='"+address1+"',audience='"+audience+"',decoration='"+decoration1+"',total='"+total+"' where eid='"+id+"'";
            prest= con.prepareStatement(sql);
            prest.execute(sql);
            int i = okcancel("Updating...");
            System.out.println(" "+i);
            new homeNext(logname).setVisible(true);
            dispose();
            con.close();
            id =0;
            }
        }catch (SQLException ex) {
            Logger.getLogger(homeNext.class.getName()).log(Level.SEVERE, null, ex);
        }   catch (ParseException ex) {
                Logger.getLogger(homeNext.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_updateActionPerformed

